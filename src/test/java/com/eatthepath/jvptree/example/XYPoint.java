package com.eatthepath.jvptree.example;

public class XYPoint {

    private final double x;
    private final double y;

    public XYPoint(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
    
    public String toString(){
		return "("+this.x+","+this.y+")";
    	
    }
}
